package task1;

public class Switch {

	public static void main(String[] args) {
		int number=30;
		switch(number) {
		case 1:
			System.out.println(10);
			break;
		case 2:
			System.out.println(20);
			break;
		case 3:
			System.out.println(30);
			default:
				System.out.println("10,20,30");
			
			
		}

	}

}
